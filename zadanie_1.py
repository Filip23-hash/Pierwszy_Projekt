# Przykład listy

lista_zakupów = ["jabłka", "banany", "chleb", "mleko", "jajka"]
print(lista_zakupów(1))  # Wyświetli "banany"
print(lista_zakupów[1])  # Wyświetli "banany"
print(lista_zakupów[0])  # Wyświetli "jabłka"
lista_zakupów.append("masło")  # Dodaje "masło" do listy
print(lista_zakupów)  # Wyświetli zaktualizowaną listę zakupów
        
